            <h3>媒体报道</h3>
            <p>2014年7月23日，上海信立会计事务所合伙人江霞接受中央二套《经济信息联播》采访，
            就全球瞩目的高通公司涉嫌反垄断调查案发表专业评论。自今年2月份国家发改委宣布对高通进行调查以来，
            高通总裁已三次来到发改委接受反垄断调查，就手机专利许可费的征收、手机捆绑销售等涉嫌违法的行为进
            行了现场调查。就此事江霞发表了自己的看法，认为这是高通公司通过各种方式来寻求一个所谓过高
            的“专利许可费率”，涉嫌构成《反垄断法》第17条第一款所禁止的“不公平高价销售”。在具有标准必要专
            利权利人滥用其支配地位收取过高许可费率时，需要《反垄断法》的介入来遏制从而达到一种平衡。
            同时邓律师认为，公开的数据显示高通的营业额可能会超过200亿美元，其中可能有一半左右是在中国获得
            的。所以如果这个案子最终是以罚款的结果，那么它一定会打破《反垄断法》从2008年生效以来所有的罚
            款记录。而迄今为止，最高的罚款记录是在去年6月份对奶粉案6.7亿人民币的处罚。</p>
