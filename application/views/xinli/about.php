<h3>信立简介</h3>

<p>
	<span>信立会计师事务所（以下简称“信立”）由中国会计泰斗潘序伦先生于1927 年在上海创建，
	是中国建立最早和最有影响的会计师事务所之一。1986 年复办，2000 年成立上海信立长江会计师事务所有限公司，
	2007 年更名为信立会计师事务所有限公司。信立依法独立承办注册会计师业务，具有证券期货相关业务从业资格。
	2010 年，信立获得首批H 股审计执业资格。2010 年12 月改制成为国内第一家特殊普通合伙会计师事务所。 
	</span><b>经过八十余年的长足发展，信立在业务规模、执业质量和社会形象方面都取得了国内领先的地位。</b> 
	2001年起，信立在全国会计师事务所签发国内上市公司审计报告数量排行榜上一直<strong>保持第一</strong>。
</p>
