<p>
	<strong><span
		style='font-family: Microsoft YaHei; color: #a57767; font-size: 14px;'>审计服务</span></strong>
</p>
<p>
	信立在完全遵守中国会计准则和审计准则的同时，更会应不同地区要求而提供附加指引，务求在各地提供一致水准的服务。<br />
	各分支机构的审计工作均使用信立专用的审计软件，整个审计过程及档案记录为电子化。<br />
	审计并非一成不变，我们希望为客户提供最好的合规和咨询服务，因此，为满足客户的需求，我们继续研发和巩固信立的审计工具。对信立的审计方法、工具和指引进行不断的拓展。
</p>
<p>
	<strong><span
		style='font-family: Microsoft YaHei; color: #a57767; font-size: 14px;'>首次公开发行</span></strong>
</p>
<p>
	信立擅长于协助各类企业在中国大陆（上海交易所和深圳证券交易所）及其他国际金融市场上市及进行其他企业活动，相关经验丰富。<br />
	我们为客户担任上市及其他融资活动的申报会计师，不仅对有关法规要求相当熟悉，更能在复杂的过程中洞悉关键问题。我们能预先确定问题所在并与客户及有关人士商讨及迅速的提出解决方案。信立的服务团队专注尽责，了解时效性对首次公开发行及其他融资活动的成功极其重要，与客户管理层及融资工作小组成员密切联系，力求在预定的时间内完成有关工作。
